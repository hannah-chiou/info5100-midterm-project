    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        // TODO add your handling code here:
        Container parent = this.getParent();
        JPanel container = (userProcessContainer != null) ? userProcessContainer : (JPanel) parent;

        container.remove(this);

        // Refresh previous card (ManageCoursesJPanel)
        Component[] comps = container.getComponents();
        if (comps.length > 0) {
            Component prev = comps[comps.length - 1];
            if (prev instanceof ManageCoursesJPanel) {
                ((ManageCoursesJPanel) prev).refreshCourseTable();
            }
        }

        CardLayout layout = (CardLayout) container.getLayout();
        layout.previous(container);
    }//GEN-LAST:event_btnBackActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String courseName = txtCourseName.getText().trim();
        String courseId = txtCourseID.getText().trim();
        String creditsText = txtCredits.getText().trim();

        if (courseName.isEmpty() || courseId.isEmpty() || creditsText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "All fields are required.");
            return;
        }

        int credits;
        try {
            credits = Integer.parseInt(creditsText);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Credits must be a valid whole number.");
            return;
        }

        if (credits <= 0) {
            JOptionPane.showMessageDialog(this, "Credits must be greater than 0.");
            return;
        }

        CourseOffer courseOffer = facultyAssignment.getCourseoffer();
        if (courseOffer == null || courseOffer.getSubjectCourse() == null) {
            JOptionPane.showMessageDialog(this, "Unable to update this course.");
            return;
        }

        if (hasDuplicateCourseIdInFaculty(courseId)) {
            JOptionPane.showMessageDialog(this, "Course ID already exists in your assigned courses.");
            return;
        }

        Course course = courseOffer.getSubjectCourse();
        course.setName(courseName);
        course.setNumber(courseId);
        course.setCredits(credits);

        JOptionPane.showMessageDialog(this, "Course updated successfully.");
    }//GEN-LAST:event_btnUpdateActionPerformed
    private boolean hasDuplicateCourseIdInFaculty(String courseId) {
        if (facultyAssignment == null || facultyAssignment.getFacultyProfile() == null) {
            return false;
        }

        for (FacultyAssignment assignment : facultyAssignment.getFacultyProfile().getFacultyassignments()) {
            if (assignment == facultyAssignment) {
                continue;
            }
            if (assignment.getCourseoffer() == null || assignment.getCourseoffer().getSubjectCourse() == null) {
                continue;
            }

            String existingId = assignment.getCourseoffer().getSubjectCourse().getNumber();
            if (courseId.equalsIgnoreCase(existingId)) {
                return true;
            }
        }

        return false;
    }

