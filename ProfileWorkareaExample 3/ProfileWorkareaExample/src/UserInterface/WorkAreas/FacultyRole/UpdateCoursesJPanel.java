    private void loadExistingCourseData() {
        if (facultyAssignment == null || facultyAssignment.getCourseoffer() == null
                || facultyAssignment.getCourseoffer().getSubjectCourse() == null) {
            return;
        }

        Course c = facultyAssignment.getCourseoffer().getSubjectCourse();
        txtCourseName.setText(c.getName());
        txtCourseID.setText(c.getNumber());
        txtCredits.setText(String.valueOf(c.getCredits()));
    }
