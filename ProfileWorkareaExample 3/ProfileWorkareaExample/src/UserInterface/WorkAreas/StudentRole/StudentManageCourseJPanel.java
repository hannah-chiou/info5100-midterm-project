    private void refreshAssignmentTable() {
    // Clear the existing table
        DefaultTableModel model = (DefaultTableModel) tblAssignments.getModel();
        model.setRowCount(0);
    
        ArrayList<File> assignments = selectedSeatAssignment.getSubmittedAssignments();
    
        if (assignments != null && !assignments.isEmpty()) {
            for (File file : assignments) {
                Object[] row = new Object[1];
                row[0] = file.getName();            
                model.addRow(row);
            }
        }
    }

