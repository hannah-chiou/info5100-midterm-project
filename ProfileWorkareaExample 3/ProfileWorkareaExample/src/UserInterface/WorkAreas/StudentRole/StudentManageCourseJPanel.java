    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblAssignments.getSelectedRow();
    String fileName = (String) tblAssignments.getValueAt(selectedRow, 0);
    
    ArrayList<File> assignments = selectedSeatAssignment.getSubmittedAssignments();
    
    File fileToRemove = null;
    for (File file : assignments) {
        if (file.getName().equals(fileName)) {
            fileToRemove = file;
            break;
        }
    }
    
        if (fileToRemove != null) {
            assignments.remove(fileToRemove);
            refreshAssignmentTable();
            attachedFile = null;
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void UploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UploadActionPerformed
    // TODO add your handling code here:
         JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileFilter(new FileNameExtensionFilter(
            "Assignment Files (*.pdf, *.doc, *.docx, *.txt)", 
            "pdf", "doc", "docx", "txt"));
    
        int returnVal = fileChooser.showOpenDialog(this);

        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            attachedFile = selectedFile;
            selectedSeatAssignment.submitAssignment(selectedFile);
            refreshAssignmentTable();
        }
    }//GEN-LAST:event_UploadActionPerformed

